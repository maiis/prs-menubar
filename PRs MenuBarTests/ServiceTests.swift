    @Test func validateHTTPResponseThrowsFor429WithRateLimit() async throws {
        let service = GitHubService(token: "test")
        let url = URL(string: "https://api.github.com")!
        let resetDate = Date().addingTimeInterval(3600)
        let response = HTTPURLResponse(
            url: url,
            statusCode: 429,
            httpVersion: nil,
            headerFields: [
                "X-RateLimit-Reset": String(Int(resetDate.timeIntervalSince1970))
            ]
        )!

        do {
            try service.validateHTTPResponse(response)
            Issue.record("Expected rate limited error")
        } catch let error as GitServiceError {
            if case let .rateLimited(date) = error {
                #expect(date != nil)
            } else {
                Issue.record("Expected rate limited error, got \(error)")
            }
        }
    }

    @Test func extractRateLimitInfoFromGitHubHeaders() async throws {
        let service = GitHubService(token: "test")
        let url = URL(string: "https://api.github.com")!
        let response = HTTPURLResponse(
            url: url,
            statusCode: 200,
            httpVersion: nil,
            headerFields: [
                "X-RateLimit-Remaining": "42",
                "X-RateLimit-Limit": "5000",
                "X-RateLimit-Reset": "1234567890"
            ]
        )!

        let rateLimit = service.extractRateLimitInfo(response)

        #expect(rateLimit?.remaining == 42)
        #expect(rateLimit?.limit == 5000)
        #expect(rateLimit?.reset != nil)
    }

