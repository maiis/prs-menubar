    @Test func stableIDsDoNotUseHashValue() async throws {
        // This test ensures we're not using hashValue anywhere in ID generation
        // hashValue is not stable across app launches

        let gitlabAccount1 = ProviderAccount(
            provider: .gitlab,
            name: "GitLab 1",
            baseURL: "https://gitlab.com/api/v4"
        )

        let gitlabAccount2 = ProviderAccount(
            provider: .gitlab,
            name: "GitLab 2",
            baseURL: "https://gitlab.com/api/v4"
        )

        // Account IDs should be different (they use UUID)
        #expect(gitlabAccount1.id != gitlabAccount2.id)

        // But keychainAccount should be deterministic based on provider and URL
        // (Actually, they include UUID too, so they'll be different)
        #expect(gitlabAccount1.keychainAccount != gitlabAccount2.keychainAccount)
